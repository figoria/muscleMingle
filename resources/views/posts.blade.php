        <h2><a href="exercise">Skull crusher</a></h2>
        <p>De skull crusher is een oefening die vaak wordt uitgevoerd met een EZ-bar. Dit is de stang die
            een beetje gekronkeld is. De bedoeling is dat je op een bankje gaat liggen en je armen met stang achter je
            hoofd houdt. Dan laat je hem een beetje naar beneden gaan en dan strek je je armen weer. Dit doe je zonder je
            ellebogen te laten verplaatsen van plek.

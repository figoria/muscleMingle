        <h2><a href="exercise">Bicep curl</a></h2>
        <p>De bicep dumbell curl is een oefening die je uitvoert met een dumbell. Je kan het zittend, maar ook staand doen.
        De bedoeling is om de dumbell op te tillen richting je schouder. Dit doe je terwijl je je elleboog stil in een
        vaste positie houdt.</p>
